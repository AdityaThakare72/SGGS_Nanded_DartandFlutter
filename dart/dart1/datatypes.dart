void main() {
  // Integers
  int number = 42;

  // Floating-point numbers
  double pi = 3.14;

  // Strings
  String message = "Hello, Dart!";

  // Booleans
  bool isDartFun = true;

  // Lists
  List<String> fruits = ['apple', 'banana', 'orange'];

  // Maps
  Map<String, dynamic> person = {
    'name': 'Alice',
    'age': 25,
    'isStudent': true,
  };

  // Printing variables with different data types
  print("Number: $number");
  print("Pi: $pi");
  print("Message: $message");
  print("Is Dart fun? $isDartFun");
  print("Fruits: $fruits");
  print("Person: $person");
}
