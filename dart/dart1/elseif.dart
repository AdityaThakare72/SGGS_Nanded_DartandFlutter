void main() {
  // elseif ladder
  double marks = 67.34;
  if (marks >= 75) {
    print('First Class with Distinction');
  } else if (marks >= 60) {
    print('First Class');
  } else if (marks >= 50) {
    print('Second Class');
  } else if (marks >= 40) {
    print('Pass Class');
  } else {
    print('Failed');
  }
}
